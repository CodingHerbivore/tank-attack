BBINT _m_tank_attack_MainGameLoop(BBINT bbt_currentLevel){
	BBINT bbt_roundTime=bbMilliSecs();
	BBINT bbt_countDown=99;
	struct brl_linkedlist_TList_obj* bbt_mapData=(struct brl_linkedlist_TList_obj*)_m_tank_attack_LoadMap(bbt_currentLevel);
	struct brl_linkedlist_TList_obj* bbt_tankList=(struct brl_linkedlist_TList_obj*)brl_linkedlist_CreateList();
	struct _m_tank_attack_TTank_obj* bbt_player1=(struct _m_tank_attack_TTank_obj*)_m_tank_attack_TTank_Create_TTTank_iiiiiiiiii(64,300,255,0,0,65,68,87,69,83);
	struct _m_tank_attack_TTank_obj* bbt_player2=(struct _m_tank_attack_TTank_obj*)_m_tank_attack_TTank_Create_TTTank_iiiiiiiiii(704,300,0,0,255,37,39,38,77,40);
	brl_linkedlist_ListAddLast((struct brl_linkedlist_TList_obj*)bbt_tankList,(BBOBJECT)bbt_player1);
	brl_linkedlist_ListAddLast((struct brl_linkedlist_TList_obj*)bbt_tankList,(BBOBJECT)bbt_player2);
	while(bbt_countDown>0){
		bbt_countDown=_m_tank_attack_UpdateCountDown((&bbt_roundTime),bbt_countDown);
		_m_tank_attack_UpdateTanks((struct brl_linkedlist_TList_obj*)bbt_mapData,(struct brl_linkedlist_TList_obj*)bbt_tankList);
		BBINT bbt_tankVictor=_m_tank_attack_UpdateBullets((struct brl_linkedlist_TList_obj*)bbt_mapData,(struct brl_linkedlist_TList_obj*)bbt_tankList);
		if(bbt_tankVictor>-1){
			_m_tank_attack_ResetGame((struct brl_linkedlist_TList_obj*)bbt_tankList,bbt_tankVictor);
		}
		brl_max2d_Cls();
		_m_tank_attack_DrawMap((struct brl_linkedlist_TList_obj*)bbt_mapData);
		_m_tank_attack_DrawTanks((struct brl_linkedlist_TList_obj*)bbt_tankList);
		_m_tank_attack_DrawInformation((struct brl_linkedlist_TList_obj*)bbt_tankList,bbt_countDown);
		brl_graphics_Flip(-1);
	}
	BBINT bbt_result=0;
	while(bbt_result==0){
		bbt_result=_m_tank_attack_DrawEndGame((struct _m_tank_attack_TTank_obj*)bbt_player1,(struct _m_tank_attack_TTank_obj*)bbt_player2);
	}
	return bbt_result;
}
